public class EskimoSib
{
    private String name, gender;
    
    public EskimoSib (String name, String gender)
    {
        this.name=name;
        this.gender=gender;
    }
    
    public String getName()
    {
        return this.name;
    }
    
    public String getGender()
    {
        return this.gender;
    }
} 